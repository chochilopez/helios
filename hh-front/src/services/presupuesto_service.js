function spfBuscarTodas () {
  return new Promise((resolve, reject) => {
    axios.get(API_URL + 'presupuesto/buscar-todas', {
      headers: {
        Authorization: 'Bearer ' + autenticacionService.obtenerToken()
      }
    })
      .then((result) => {
        resolve(result)
      })
      .catch((error) => {
        reject(error)
      })
  })
}

