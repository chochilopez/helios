function spfBuscarTodasConSesion (sesion) {
  return new Promise((resolve, reject) => {
    axios.get(API_URL + 'proveedor/buscar-todas', {
      headers: {
        Authorization: 'Bearer ' + autenticacionService.obtenerToken()
      }
    })
      .then((result) => {
        if (result.status === 200) {
          llaveroService.guardarEnLocalConSesion('hhProveedorTodasConSesion', result.data, sesion)
        }
        resolve(result)
      })
      .catch((error) => {
        reject(error)
      })
  })
}

