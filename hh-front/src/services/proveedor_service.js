function spfBuscarTodasPorDireccion (direccion) {
  return new Promise((resolve, reject) => {
    axios.get(API_URL + 'proveedor/buscar-todas-por-direccion/' + direccion, {
      headers: {
        Authorization: 'Bearer ' + autenticacionService.obtenerToken()
      }
    })
      .then((result) => {
        resolve(result)
      })
      .catch((error) => {
        reject(error)
      })
  })
}

